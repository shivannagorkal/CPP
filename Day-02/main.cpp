#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;

    // Calculate the sum of the two numbers
    int sum = a + b;

    // Output the result
    cout << "The sum of " << a << " and " << b << " is: " << sum << endl;

    return 0;
}