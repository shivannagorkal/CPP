// FOR LOOP (Most Used in CP)
// syntax:
// for(initialization; condition; update)
// {
//     // code
// }

// Example: Print first 10 natural numbers
#include <iostream>
using namespace std;

int main()
{

    // Example: Print first 10 natural numbers
    
    for(int i = 1; i <= 10; i++)
    {
        cout << i << " ";
    }

    return 0;
}